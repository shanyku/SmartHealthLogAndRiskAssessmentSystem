// 1. 決策樹計算函式
function calculateRisk(sleep, steps, mood) {
    if (sleep < 6) {
        if (steps < 4000) {
            return 'High';
        } else {
            return mood < 5 ? 'High' : 'Medium';
        }
    } else {
        if (steps >= 7000 && mood >= 6) {
            return 'Low';
        } else {
            return 'Medium';
        }
    }
}

