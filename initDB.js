async function init() {
    console.log('正在初始化資料庫並自動進行決策樹風險評估...');
    
    const db = await open({
        filename: './database.db',
        driver: sqlite3.Database
    });

    // 重新建立全新的資料表（如果本來有就先刪除掉，確保資料最乾淨）
    await db.run(`DROP TABLE IF EXISTS health_logs`);
    
    await db.run(`
        CREATE TABLE health_logs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            log_date TEXT UNIQUE,
            sleep_hours REAL,
            steps INTEGER,
            mood_score INTEGER,
            risk_level TEXT
        )
    `);
    console.log('✅ 資料表 health_logs 建立成功！');

    // 逐筆計算風險並匯入
    for (const row of rawData) {
        const [log_date, sleep_hours, steps, mood_score] = row;
        
        // 核心關鍵：匯入前，先呼叫決策樹模型算出風險等級！
        const risk_level = calculateRisk(sleep_hours, steps, mood_score);

        await db.run(`
            INSERT INTO health_logs (log_date, sleep_hours, steps, mood_score, risk_level)
            VALUES (?, ?, ?, ?, ?)
        `, [log_date, sleep_hours, steps, mood_score, risk_level]);
    }

    console.log(`🎉 成功自動評估並匯入 ${rawData.length} 筆健康日誌資料！`);
    await db.close();
}

init().catch(err => console.error('發生錯誤：', err));